package com.walmart.oms.dc.dcinfo.model;

public class DcInfo {
	private int store;
	private int item;
	private int dncbr;
	public int getStore() {
		return store;
	}
	public void setStore(int store) {
		this.store = store;
	}
	public int getItem() {
		return item;
	}
	public void setItem(int item) {
		this.item = item;
	}
	public int getDncbr() {
		return dncbr;
	}
	public void setDncbr(int dncbr) {
		this.dncbr = dncbr;
	}
	

}
